/**
 * @param {string} s
 * @param {string} t
 * @return {boolean}
 */
var isAnagram = function (s, t) {
  // (s = "anagram"), (t = "nagaram");
  // true
  // (s = "rat"), (t = "car");
  //  false

  const sArr = s.split("");
  const tArr = t.split("");

  if (sArr.length !== tArr.length) return false;

  for (let x = 0; x < tArr.length; x++) {
    const letter = tArr[x];
    let isFound = false;

    for (let y = 0; y < sArr.length; y++) {
      if (sArr[y] === letter) {
        sArr.splice(y, 1);
        isFound = true;
        break;
      }
    }

    if (!isFound) return false;
  }

  return true;
};
